CREATE DATABASE BOOK_STORE;

USE BOOK_STORE;

CREATE TABLE BOOKS (
BOOK_ID  INT PRIMARY KEY ,
TITLE VARCHAR(100),
AUTHOR VARCHAR(100),
GENRE VARCHAR(150),
PUBLISHED_YEAR INT,
PRICE NUMERIC (10,2),
STOCK INT
);

CREATE TABLE CUSTOMERS (
CUSTOMER_ID INT PRIMARY KEY,
NAME VARCHAR(100),
EMAIL VARCHAR(100),
PHONE INT,
CITY VARCHAR(150),
COUNTRY VARCHAR(200)
);

CREATE TABLE ORDERS (
ORDER_ID INT PRIMARY KEY,
CUSTOMER_ID INT ,
BOOK_ID INT,
ORDER_DATE DATE,
QUANTITY INT ,
TOTAL_AMOUNT NUMERIC (10,2),
FOREIGN KEY(CUSTOMER_ID) REFERENCES CUSTOMERS(CUSTOMER_ID),
FOREIGN KEY(BOOK_ID) REFERENCES BOOKS(BOOK_ID)
);

