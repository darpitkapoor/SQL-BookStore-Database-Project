#>>>>>>>  ADVANCE QUERIES

#1) List customers who have placed at least 2 orders

SELECT C.CUSTOMER_ID,C.NAME,COUNT(O.ORDER_ID) AS TOTAL_ORDERS
FROM CUSTOMERS C
JOIN ORDERS O ON
C.CUSTOMER_ID=O.CUSTOMER_ID
GROUP BY C.CUSTOMER_ID,C.NAME
HAVING COUNT(O.ORDER_ID)>=1;

#2) Find the most frequently ordered book

SELECT B.BOOK_ID,B.TITLE , COUNT(O.ORDER_ID) MOST_ORDERED_BOOK
FROM BOOKS B
JOIN ORDERS O ON
B.BOOK_ID=O.BOOK_ID
GROUP BY B.BOOK_ID
ORDER BY MOST_ORDERED_BOOK DESC
LIMIT 1;

#3) List the cities where customers who spent over $30 are located

SELECT DISTINCT C.CITY,O.TOTAL_AMOUNT
FROM CUSTOMERS C 
JOIN ORDERS O ON 
C.CUSTOMER_ID=O.CUSTOMER_ID
WHERE O.TOTAL_AMOUNT > 30;

#4) Find the customer who spent the most on orders

SELECT C.CUSTOMER_ID , C.NAME , SUM(O.TOTAL_AMOUNT) AS TOP_CUSTOMER
FROM CUSTOMERS C
JOIN ORDERS O ON
C.CUSTOMER_ID=O.CUSTOMER_ID
GROUP BY C.CUSTOMER_ID,C.NAME
ORDER BY TOP_CUSTOMER DESC
LIMIT 1;

#5) Calculate the stock remaining after fulfilling all orders

SELECT B.BOOK_ID,B.TITLE,B.STOCK,COALESCE(SUM(O.QUANTITY),0) AS ORDER_QUANTITY,
B.STOCK-COALESCE(SUM(O.QUANTITY),0) AS REMAINING_QUANTITY
FROM BOOKS B
LEFT JOIN ORDERS O ON
B.BOOK_ID=O.BOOK_ID
GROUP BY B.BOOK_ID,B.TITLE;

